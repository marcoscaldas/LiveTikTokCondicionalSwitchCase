let fruta = "uva";

switch(fruta){

    case "uva":
        console.log("Você escolheu um uva");       
        break;

    case "banana":
        console.log("Você escolheu uma banana");
        break;
    
    case "laranja":
        console.log("Você escolheu uma laranja");
        break;

    default:
        console.log("Fruta não encontrada."); 
        break;
}